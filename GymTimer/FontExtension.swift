import Foundation
import UIKit

extension UIFont {
    
    class func smallAvenir() -> UIFont {
        return UIFont(name: "Avenir", size: 12)!
    }
    
    class func mediumAvenir() -> UIFont {
        return UIFont(name: "Avenir", size: 16)!
    }
    
    class func largeAvenir() -> UIFont {
        return UIFont(name: "Avenir", size: 26)!
    }
    
    class func hugeAvenir() -> UIFont {
        return UIFont(name: "Avenir", size: 60)!
    }
}
